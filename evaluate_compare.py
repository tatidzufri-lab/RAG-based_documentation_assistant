# ─── Сбор ответов ────────────────────────────────────────────────────

def collect_answers(pipeline, model_name, questions, ground_truths):
    """Прогоняет пайплайн по вопросам и возвращает Dataset для RAGAS."""
    q_list, a_list, ctx_list, gt_list = [], [], [], []

    print(f"\n[*] Сбор ответов от {model_name} ({len(questions)} вопросов)...\n")

    for i, question in enumerate(questions, 1):
        print(f"  {i}/{len(questions)}: {question[:60]}...")
        result = pipeline.query(question, use_cache=False)

        q_list.append(question)
        a_list.append(result["answer"])
        ctx_list.append([doc["text"] for doc in result["context_docs"]])

        gt = ground_truths[i - 1] if i - 1 < len(ground_truths) and ground_truths[i - 1] else result["answer"][:100]
        gt_list.append(gt)

    return Dataset.from_dict({
        "user_input": q_list,
        "response": a_list,
        "retrieved_contexts": ctx_list,
        "reference": gt_list,
    })


# ─── Вспомогательные функции ─────────────────────────────────────────

def avg_metric(values):
    clean = [v for v in values if not (isinstance(v, float) and math.isnan(v))]
    return sum(clean) / len(clean) if clean else 0


def fmt(val):
    if isinstance(val, float) and math.isnan(val):
        return "н/д"
    return f"{val:.4f}"


# ─── Главная функция ─────────────────────────────────────────────────

def main():
    if not os.getenv("OPENAI_API_KEY"):
        print("[ОШИБКА] OPENAI_API_KEY не задан в .env")
        sys.exit(1)

    print("=" * 70)
    print("СРАВНИТЕЛЬНАЯ ОЦЕНКА ChatGPT vs GigaChat (RAGAS)")
    print("=" * 70)

    # --- Инициализация пайплайнов ---
    pipelines = {}

    if os.getenv("OPENAI_API_KEY"):
        try:
            print("\n[1/2] Инициализация ChatGPT...")
            pipelines['ChatGPT'] = init_api_pipeline()
            print("[+] ChatGPT готов")
        except Exception as e:
            print(f"[-] ChatGPT недоступен: {e}")

    if os.getenv("GIGACHAT_AUTH_KEY") and os.getenv("GIGACHAT_RQUID"):
        try:
            print("\n[2/2] Инициализация GigaChat...")
            pipelines['GigaChat'] = init_giga_pipeline()
            print("[+] GigaChat готов")
        except Exception as e:
            print(f"[-] GigaChat недоступен: {e}")

    if not pipelines:
        print("\nНи одна модель не инициализирована.")
        sys.exit(1)

    # --- Сбор ответов от каждой модели ---
    datasets = {}
    for name, pipeline in pipelines.items():
        datasets[name] = collect_answers(pipeline, name, EVALUATION_QUESTIONS, GROUND_TRUTHS)

    # --- Настройка RAGAS (OpenAI как «судья») ---
    openai_client = OpenAI()
    ragas_llm = llm_factory('gpt-4o-mini', client=openai_client)
    ragas_emb = LCOpenAIEmbeddings(model='text-embedding-3-small')

    metrics = [
        Faithfulness(llm=ragas_llm),
        ContextPrecision(llm=ragas_llm),
        AnswerRelevancy(llm=ragas_llm, embeddings=ragas_emb),
    ]

    # --- Оценка каждой модели ---
    results = {}
    for name, dataset in datasets.items():
        print(f"\n{'='*70}")
        print(f"[*] Оценка {name} через RAGAS...")
        print(f"{'='*70}")
        try:
            results[name] = evaluate(dataset=dataset, metrics=metrics)
        except Exception as e:
            print(f"[ОШИБКА] Оценка {name} не удалась: {e}")

    if not results:
        print("\nОценка не удалась ни для одной модели.")
        sys.exit(1)

    # ─── Вывод сравнительных результатов ──────────────────────────────

    model_names = list(results.keys())
    metric_keys = ['faithfulness', 'context_precision', 'answer_relevancy']
    metric_labels = {
        'faithfulness':       'Faithfulness',
        'context_precision':  'Context Precision',
        'answer_relevancy':   'Answer Relevancy',
    }

    print("\n" + "=" * 70)
    print("СВОДНАЯ ТАБЛИЦА")
    print("=" * 70)

    # Средние по метрикам
    avgs = {}
    for name in model_names:
        avgs[name] = {k: avg_metric(results[name][k]) for k in metric_keys}

    header = f"{'Метрика':<25}" + "".join(f"{n:>15}" for n in model_names)
    if len(model_names) == 2:
        header += f"{'Разница':>12}"
    print(f"\n{header}")
    print("─" * len(header))

    for key in metric_keys:
        row = f"{metric_labels[key]:<25}"
        vals = [avgs[n][key] for n in model_names]
        for v in vals:
            row += f"{v:>15.4f}"
        if len(vals) == 2:
            diff = vals[0] - vals[1]
            sign = "+" if diff > 0 else ""
            row += f"{sign}{diff:>11.4f}"
        print(row)

    # Средний балл
    print("─" * len(header))
    row = f"{'СРЕДНИЙ БАЛЛ':<25}"
    scores = []
    for name in model_names:
        s = sum(avgs[name].values()) / len(metric_keys)
        scores.append(s)
        row += f"{s:>15.4f}"
    if len(scores) == 2:
        diff = scores[0] - scores[1]
        sign = "+" if diff > 0 else ""
        row += f"{sign}{diff:>11.4f}"
    print(row)

    # Победитель
    if len(model_names) == 2:
        if scores[0] > scores[1]:
            winner = model_names[0]
        elif scores[1] > scores[0]:
            winner = model_names[1]
        else:
            winner = "Ничья"
        print(f"\nЛидер: {winner}")

    # --- Результаты по блокам ---
    print("\n" + "=" * 70)
    print("РЕЗУЛЬТАТЫ ПО БЛОКАМ")
    print("=" * 70)

    for (start, end), block_name in BLOCK_NAMES.items():
        print(f"\n  {block_name}")
        for name in model_names:
            block_avg = {}
            for key in metric_keys:
                vals = results[name][key][start:end]
                block_avg[key] = avg_metric(vals)
            total = sum(block_avg.values()) / len(metric_keys)
            parts = " | ".join(f"{metric_labels[k][:5]}: {block_avg[k]:.2f}" for k in metric_keys)
            print(f"    {name:>10}: {parts} | Avg: {total:.2f}")

    # --- Детали по вопросам ---
    print("\n" + "=" * 70)
    print("ДЕТАЛЬНЫЕ РЕЗУЛЬТАТЫ ПО ВОПРОСАМ")
    print("=" * 70)

    for i, question in enumerate(EVALUATION_QUESTIONS):
        print(f"\n{i+1}. {question}")
        for name in model_names:
            f_val = fmt(results[name]['faithfulness'][i])
            cp_val = fmt(results[name]['context_precision'][i])
            ar_val = fmt(results[name]['answer_relevancy'][i])
            print(f"   {name:>10}: Faith={f_val}  CtxPrec={cp_val}  AnsRel={ar_val}")

    # --- Экспорт в CSV ---
    csv_path = str(ROOT_DIR / f"ragas_comparison_{int(time.time())}.csv")
    with open(csv_path, 'w', newline='', encoding='utf-8') as f:
        writer = csv.writer(f)

        headers = ['#', 'Вопрос']
        for name in model_names:
            headers.extend([
                f'{name}_Faithfulness',
                f'{name}_ContextPrecision',
                f'{name}_AnswerRelevancy',
                f'{name}_Answer',
            ])
        headers.append('Ground_Truth')
        writer.writerow(headers)

        for i, question in enumerate(EVALUATION_QUESTIONS):
            row = [i + 1, question]
            for name in model_names:
                row.append(fmt(results[name]['faithfulness'][i]))
                row.append(fmt(results[name]['context_precision'][i]))
                row.append(fmt(results[name]['answer_relevancy'][i]))
                row.append(datasets[name]['response'][i])
            row.append(GROUND_TRUTHS[i] if i < len(GROUND_TRUTHS) else "")
            writer.writerow(row)

    print(f"\n{'='*70}")
    print(f"[OK] Результаты сохранены в: {csv_path}")
    print(f"{'='*70}\n")


